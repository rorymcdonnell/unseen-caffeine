import crypto from "crypto";
import nodemailer from "nodemailer";
import asyncHandler from "express-async-handler";
import generateToken from "../utils/generateToken.js";
import User from "../models/userModel.js";

// @desc    Auth user & get token
// @route   POST /api/users/login
// @access  Public
const authUser = asyncHandler(async (req, res) => {
  const { email, password } = req.body;

  const user = await User.findOne({ email, isActive: true });

  if (user && (await user.matchPassword(password))) {
    res.json({
      _id: user._id,
      name: user.name,
      email: user.email,
      isAdmin: user.isAdmin,
      token: generateToken(user._id),
    });
  } else {
    res.status(401);
    throw new Error("Invalid email or password");
  }
});

// @desc    Register a new user
// @route   POST /api/users
// @access  Public
const registerUser = asyncHandler(async (req, res) => {
  const { name, email, password } = req.body;

  const userExists = await User.findOne({ email });

  if (userExists) {
    res.status(400);
    throw new Error("User already exists");
  }

  const user = await User.create({
    name,
    email,
    password,
  });

  if (user) {
    const token = crypto.randomBytes(20).toString("hex");
    user.confirmationToken = token;

    const transporter = nodemailer.createTransport({
      host: "smtp.gmail.com",
      port: 587,
      auth: {
        user: process.env.HOST_EMAIL, // generated ethereal user
        pass: process.env.HOST_EMAIL_PASSWORD, // generated ethereal password
      },
    });
    const mailOptions = {
      from: "unseencaffeinedev@gmail.com",
      to: `${user.email}`,
      subject: "Email Confirmation",
      text:
        "Thank you for registering with Unseen Caffeine.Â You can now login and use your account.\n\n" +
        "Please click on the following link, or paste this into your browser to complete the registration process:\n\n" +
        `http://localhost:3000/confirm-email/${token}\n\n`,
    };
    // send mail with defined transport object
    await transporter.sendMail(mailOptions);
    await user.save();
    res.status(201).json({
      message:
        "A confirmation email has been sent to your given email address, please confirm your email to complete the registration process",
    });
  } else {
    res.status(400);
    throw new Error("Invalid user data");
  }
});

// @desc Confirm Email Token
// @route GET /api/users/emailConfirmation/token
// @access Public

const emailConfirmation = asyncHandler(async (req, res) => {
  let user = await User.findOne({ confirmationToken: req.params.token });
  if (!user) {
    return res
      .status(400)
      .json({ message: "There's problem to confirm your email address" });
  } else {
    user.isActive = true;
    user.confirmationToken = "";
    await user.save();
    res
      .status(200)
      .json({ message: "User Registration Completed Successfully!" });
  }
});

// @desc    Get user profile
// @route   GET /api/users/profile
// @access  Private
const getUserProfile = asyncHandler(async (req, res) => {
  const user = await User.findById(req.user._id);

  if (user) {
    res.json({
      _id: user._id,
      name: user.name,
      email: user.email,
      isAdmin: user.isAdmin,
    });
  } else {
    res.status(404);
    throw new Error("User not found");
  }
});

// @desc    Update user profile
// @route   PUT /api/users/profile
// @access  Private
const updateUserProfile = asyncHandler(async (req, res) => {
  const user = await User.findById(req.user._id);

  if (user) {
    user.name = req.body.name || user.name;
    user.email = req.body.email || user.email;
    if (req.body.password) {
      user.password = req.body.password;
    }

    const updatedUser = await user.save();

    res.json({
      _id: updatedUser._id,
      name: updatedUser.name,
      email: updatedUser.email,
      isAdmin: updatedUser.isAdmin,
      token: generateToken(updatedUser._id),
    });
  } else {
    res.status(404);
    throw new Error("User not found");
  }
});

// @desc    Get all users
// @route   GET /api/users
// @access  Private/Admin
const getUsers = asyncHandler(async (req, res) => {
  const users = await User.find({});
  res.json(users);
});

// @desc    Delete user
// @route   DELETE /api/users/:id
// @access  Private/Admin
const deleteUser = asyncHandler(async (req, res) => {
  const user = await User.findById(req.params.id);

  if (user) {
    await user.remove();
    res.json({ message: "User removed" });
  } else {
    res.status(404);
    throw new Error("User not found");
  }
});

